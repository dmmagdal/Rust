fn main() {
    // main() function is the first code that is always executed in a
    // rust program.

    // To stick to standard style for the code, use rustfmt tool to
    // format to your particular style.

    // Rust is indented with 4 spaces (no tabs).

    // prinln! is a rust macro signified by the "!" at the end.

    // "hello, world!" is a string and is passed as an argument to the 
    // println!() macro.

    // Prints "hello, world!" to terminal.
    println!("Hello, world!");

    // Compile with rustc main.rs. This should give you an executable 
    // program of main.rs (seen as either main or main.exe).
}
